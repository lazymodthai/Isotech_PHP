เครื่อง ECG Simulator รุ่น PS-2010 เป็นหนึ่งใน PS-2000 Series โดยมี Feature หลักๆดังนี้<br>
<br>
• ขนาดเล็กน้ำหนักเบาพกพาสะดวก<br>
• จุดเชื่อมต่อแบบ Universal 10 patient lead<br>
• จำลองสัญญาณ ECG แบบปกติ<br>
• จำลองสัญญาณ ECG Performance<br>
• High level output (1 v p-p)<br>
• Lead test function<br>
• ใช้งานกับแบตเตอรี่ 9V พร้อมไฟแสดงสถานะ<br>
• ปุ่มกดใช้งานง่าย<br>
• Amplitude accuracy: ± 2%<br>
• Frequency accuracy: ± 0.5% of setting