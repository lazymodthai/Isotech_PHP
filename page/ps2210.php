เครื่อง Patient Simulator รุ่น PS-2210 เป็นหนึ่งใน PS-2200 Series โดยมี Feature หลักๆดังนี้<br>
<br>
• ขนาดเล็กน้ำหนักเบาพกพาสะดวก<br>
• หน้าจอแสดงผลแบบ Backlight<br>
• จุดเชื่อมต่อแบบ Universal 10 patient lead<br>
• จำลองสัญญาณ ECG แบบปกติ<br>
• จำลองสัญญาณ ECG Performance ได้<br>
• จำลองสัญญาณ IBP ได้ (1 Chanel)<br>
• จำลองอัตราการหายใจได้ Respiration (Lead LA or LL)<br>
• จำลองอุณหภูมิได้เมื่อใช้กับสาย YSI Series 400 หรือ 700<br>
• ใช้งานร่วมกับ SpO2 Simulator ได้ (อุปกรณ์เสริม)<br>
• สามารถทดสอบ Cardiac Output ได้ (อุปกรณ์เสริม)<br>
• สามารถจำลอง Fetal/Meternal ได้ (อุปกรณ์เสริม)<br>
• ใช้งานกับแบตเตอรี่ 9V พร้อมไฟแสดงสถานะ<br>
• ปุ่มกดใช้งานง่าย