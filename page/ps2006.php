เครื่อง ECG Simulator รุ่น PS-2006 เป็นหนึ่งใน PS-2000 Series โดยมี Feature หลักๆดังนี้<br>
<br>
• ขนาดเล็กน้ำหนักเบาพกพาสะดวก<br>
• จุดเชื่อมต่อแบบกระดุม 6 patient lead<br>
• จำลองสัญญาณ ECG แบบปกติ และ PVC Arrhythmia<br>
• จำลองสัญญาณ ECG Performance<br>
• ใช้งานกับแบตเตอรี่ 9V พร้อมไฟแสดงสถานะ<br>
• ปุ่มกดใช้งานง่าย<br>
• Amplitude accuracy: ± 2%<br>
• Frequency accuracy: +/- 0.5% of setting