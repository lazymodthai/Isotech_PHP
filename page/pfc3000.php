The PFC-3000 series of ventilator testers measures flow, pressure, temperature,
humidity and O2 concentrations bidirectionally. The one-of-a-kind adult,
pediatric and high frequency ventilation measuring modes make the PFC-3000 the
ideal calibration tool for all ventilators, anesthesia machines and spirometers.
The PFC-3000 distinguishes itself from other calibration tools by combining a
simple, intuitive multilingual user interface with the highest precision tester.
With the push of a button, all measured values can be stored directly on the
PFC-3000 and later retrieved for documentation purposes.
