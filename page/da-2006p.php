เครื่องวิเคราะห์เครื่องกระตุกหัวใจ รุ่น DA-2006P มี Feature หลักๆดังนี้<br>
• มีการแสดงผลหน้าจอ Backlight แบบหลายพารามิเตอร์<br>
• แสดงผล Waveform ของ Defibrillator และ Pacemaker บนหน้าจอ<br>
• สามารถทดสอบทั้งแบบ Monophasic และ Biphasic<br>
• รองรับการทดสอบสูงสุด 5000 โวลต์, 1000 จูลล์<br>
• สามารถวัด Cardioversion delay<br>
• สามารถวัด Charge time<br>
• บันทึก Waveform ได้<br>
• การเชื่อมต่อ ECG แบบ 12 lead<br>
• สามารถจำลองสัญญาณ ECG แบบปกติ และผิดปกติได้<br>
• เชื่อมต่อ Printer ด้วยพาราเรลพอร์ท<br>
• ทำงานด้วยแบตเตอรี่ 9V และมีไฟแสดงสถานะเมื่อแบตเตอรี่ต่ำ<br>
• สามารถทดสอบแบบ Auto sequence<br>
• เลือก Load สำหรับการทดสอบได้ 26 ค่า (เฉพาะรุ่น DA-2006P)<br>
• Full Pulse Analysis (เฉพาะรุ่น DA-2006P)<br>
• สามารถทดสอบ Demand Sensitive (เฉพาะรุ่น DA-2006P)<br>
• สามารถทดสอบ Refractory Period (เฉพาะรุ่น DA-2006P)<br>
• สามารถจำลองสัญญาณรบกวน 50/60 Hz (เฉพาะรุ่น DA-2006P)<br>
• การปกป้อง Pacer Input (เฉพาะรุ่น DA-2006P)