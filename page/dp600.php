เครื่องวิเคราะห์เครื่องกระตุกหัวใจ รุ่น DP-600 มี Feature หลักๆดังนี้<br />
<br />
• เครื่องกระตุ้นหัวใจแบบ single-, dual- และ triple-circuit<br />
• การวัดค่า defi-impulses ขั้วอิสระ<br />
• แสดงผล Discharge เป็นแบบกราฟฟิค<br />
• หน้าจอทัชสกรีนหรือควบคุมด้วยคอมพิวเตอร์<br />
• การจำลองสัญญาณ ECG<br />
• นาฬิกาจับเวลาสำหรับการ Charge และ Discharge
