• มีหน่วยการวัด PSI, inH2O, cmH2O และ mmHg<br>
• การแสดงผลหน้าจอ LCD แบบ 5 digit<br>
• ใช้งานกับแบตเตอรี่ 9V แสดงสถานะ 0-100%