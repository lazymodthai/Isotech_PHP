The AA-8100 is an enhanced version of the AA-8000, adding Auto Agent ID,
Bluetooth, iOS and Android Apps, CO2 Gas Analysis and 100 DUT Tags -
downloadable from a PC, tablet or smartphone. It was designed to meet the demand
for a more advanced, small, easy to use unit with high reliability and accuracy.
Utilizing proprietary state-of-the-art digital NDIR (Non-Dispersive Infrared)
Technology it provides a cost effective, high function, microprocessor-based
analyzer that is simple to operate while maintaining high performance and
accuracy.<br />
<br />
The unit’s flexible design, portable size and Bluetooth smart device interface
make it the ideal service tool for anesthesia service applications in hospitals
and surgical centers. It has a slanted display and adjustable handle/stand to
allow for ease of viewing. The internal rechargeable Lithium battery provides
for hours of continuous operation.<br />
<br />
Auto Agent ID or Manual One Button selection of the Agent to be analyzed coupled
with One Button control over the sampling pump make the unit simple and quick to
use. No complex menus and key combinations are required.<br />
<br />
The internal data logger combined with the internal clock allows for the storage
of up to 1000 time and date stamped readings, that can be downloaded to a
computer using the USB interface or smart device via Bluetooth. The AA-8100 may
be operated remotely via USB or Bluetooth.<br />
