เครื่องวิเคราะห์ความปลอดภัยทางไฟฟ้า รุ่น GM-300 มี Feature หลักๆดังนี้<br>
<br>
• การควบคุมด้วยเคอเซอร์ หรือควบคุมด้วยคอมพิวเตอร์<br>
• มีช่องเชื่อมต่อคีย์บอร์ดและบาร์โค้ด<br>
• หน่วยความจำภายในสำหรับการวัด 125 รายการหรือข้อมูลอุปกรณ์<br>
• การวัดแบบอัตโนมัติหรือมัลติมิเตอร์<br>
• 25A PE Test ตามมาตรฐาน IEC 60601