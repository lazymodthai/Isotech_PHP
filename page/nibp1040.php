เครื่องจำลองสัญญาณชีพ รุ่น NIBP-1040 มี Feature หลักๆดังนี้<br>
• หน้าจอแสดงผลขนาดใหญ่ แสดงผลหลายพารามิเตอร์<br>
• สามารถใช้งานเป็น Manometer วัดแรงดันที่ ± 500 mmHg<br>
• สามารถจำลองสัญญาณ NIBP ในโหมด Adult, Neonatal, Hypertensive และ Hypotensive<br>
• ค่า Accuracy ± 1% ของค่าที่อ่านได้<br>
• สามารถกำหนดค่า Envelope Shift ได้ทั้ง Systolic และ Diastolic<br>
• สามารถแสดงค่า Peak Pressure ได้<br>
• สามารถทดสอบการรั่วไหล (Leak rate test) ได้<br>
• ใช้งานด้วย Battery ในตัวและสามารถชาร์จได้<br>
• สามารถจำลองสัญญาณ ECG ปกติ และ ผิดปกติได้ (ECG arrhythmia)<br>
• มี ECG arrhythmia sequence สำหรับทดสอบ Alarm<br>
• สามารถจำลองสัญญาณ Pace Waveform<br>
• สามารถกำหนดค่าการจำลองสัญญาณ IBP ได้<br>
• สามารถจำลองอัตราการหายใจ<br>
• สามารถจำลองอุณหภูมิที่ใช้กับ YSI 400 และ 700<br>
• สามารถใช้ทดสอบ Cadiac Output ได้ (ใช้ร่วมกับ MCO-2100 breakout box)<br>
• สามารถจำลองสัญญาณ Fetal/Maternal